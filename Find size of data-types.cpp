#include<iostream>
using namespace std;

main()
{
//	'SIZEOF()' --> TO FIND SIZE
	int a;
	char b;
	float c;
	double d;
	cout<<"\nSize of char : "<<sizeof(b)<<" bytes";
	cout<<"\nSize of int : "<<sizeof(a)<<" bytes";
	cout<<"\nSize of float : "<<sizeof(c)<<" bytes";
	cout<<"\nSize of double : "<<sizeof(d)<<" bytes";
}
